package com.example.myapplication.standart_programlama

fun main() {

    for (x in 1 .. 3 ){
        println(x)
    }

    for (x in 10 .. 30 step 5 ){
        println(x)
    }

    for (x in 20 downTo 10 step 5 ){
        println(x)
    }
    var sayac = 1
    while(sayac <4){
        println(sayac)
        sayac++
    }



}